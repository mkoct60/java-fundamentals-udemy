package com.sh;

public class Main {

    public static void main(String[] args) {
        int firstValue = 5 / 3;
        float floatValue = 5f / 3f;
        double doubleValue = 5d / 3d;

        System.out.println("int is: " + firstValue);
        System.out.println("float is: " + floatValue);
        System.out.println("double is: " + doubleValue);
    }
}