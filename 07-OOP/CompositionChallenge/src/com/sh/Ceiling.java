package com.sh;

public class Ceiling {
    private int height;
    private int painedColor;

    public Ceiling(int height, int painedColor) {
        this.height = height;
        this.painedColor = painedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPainedColor() {
        return painedColor;
    }
}
